# 1- Assignment Operators

# Addition
print(f'{a} + {b}: ', a + b)

# Subtraction
print(f'{a} - {b}: ', a - b)

# Multiplication
print(f'{a} * {b}: ', a * b)

# Division
print(f'{a} / {b}: ', a / b)

# Modulus
# a = 15
# b = 5
print(f'{a} % {b}: ', a % b)

# Exponentiation
# a = 5
# b = 2
print(f'{a} ** {b}: ', a ** b)

# Floor division
a = 13
b = 5
print(f'{a} / {b}: ', a / b)
print(f'{a} // {b}: ', a // b)
